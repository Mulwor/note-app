// Фильтрация объектов
const users = [
  { name: 'Алексей', age: 25 },
  { name: 'Мария', age: 17 },
  { name: 'Иван', age: 30 }
];

const adults = users.filter(user => user.age >= 18);
console.log(adults); 
// [{ name: 'Алексей', age: 25 }, { name: 'Иван', age: 30 }]

