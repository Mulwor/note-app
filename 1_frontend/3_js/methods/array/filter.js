// Поиск по строке, содержащие букву "а":
const fruits = ['яблоко', 'банан', 'апельсин', 'груша'];
const fruitsWithA = fruits.filter(fruit => fruit.includes('а'));
console.log(fruitsWithA); // ['яблоко', 'банан', 'груша']