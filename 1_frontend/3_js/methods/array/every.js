// ? Метод every() проверяет, все ли элементы массива удовлетворяют
// ? заданному условию (передаваемому в виде функции-предиката).

// * Синтаксис: array.every(callback(element, index, array), thisArg)

// ? Проверка является ли каждое число четным
const numbers = [2, 4, 6, 8, 10];
numbers.every(num => num % 2 === 0);      // true

const mixedNumbers = [2, 4, 5, 8];
mixedNumbers.every(num => num % 2 === 0); // false

// ! ================================================================
const users = [
  { name: 'Alice', age: 25 },
  { name: 'Bob', age: 30 },
  { name: 'Charlie', age: 22 }
];

// Все ли пользователи старше 18 лет?
users.every(user => user.age >= 18);      // true

// ! ================================================================
const array = [10, 20, 30, 40];

// Все ли элементы больше своего индекса?
array.every((num, index) => num > index);    // true (10>0, 20>1, 30>2, 40>3)

